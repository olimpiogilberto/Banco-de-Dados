package CAP09;

public class Tarea {

  public long Area(int X) {
    long AREA = X * X;
    return(AREA);
  }

  public double Area(float R, float H) {
    double AREA = R * R * 3.14159 * H;
    return(AREA);
  }

  public int Area(int X, int Y, int Z) {
    int AREA = X * Y * Z;
    return(AREA);
  }

}
